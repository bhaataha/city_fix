export type TranslationKey = keyof typeof translations.he;

export function translate(language: Language, key: TranslationKey, values?: Record<string, string | number>): string {
  let value = translations[language][key] || translations[DEFAULT_LANGUAGE][key] || key;
  if (!values) return value;

  for (const [name, replacement] of Object.entries(values)) {
    value = value.replaceAll(`{${name}}`, String(replacement));
  }

  return value;
}
